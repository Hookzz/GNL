#include "libft.h"

int		ft_count_words(const char *s, char c)
{
	int i;
	int words;

	i = 0;
	words = 0;
	while (s[i])
	{
		if (s[i] != c)
		{
			words++;
			while (s[i] != c)
				i++;
		}
		i++;
	}
	return (words);
}
